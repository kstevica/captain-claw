# ── Manager ──────────────────────────────────────────────────────────


class SisterSessionManager:
    """SQLite-backed task queue and briefing store for sister sessions."""

    def __init__(self, db_path: Path | str | None = None):
        cfg = get_config()
        if db_path is None:
            db_path = Path(cfg.sister_session.db_path).expanduser()
        self._db_path = Path(db_path)
        self._db_path.parent.mkdir(parents=True, exist_ok=True)
        self._db: aiosqlite.Connection | None = None

    async def _get_db(self) -> aiosqlite.Connection:
        if self._db is None:
            self._db = await aiosqlite.connect(str(self._db_path))
            self._db.row_factory = aiosqlite.Row
            await self._db.execute("PRAGMA journal_mode=WAL")
            await self._db.execute("PRAGMA busy_timeout=5000")
            await self._init_schema()
        return self._db

    async def _init_schema(self) -> None:
        db = self._db
        assert db is not None
        await db.executescript("""
            CREATE TABLE IF NOT EXISTS proactive_tasks (
                id              TEXT PRIMARY KEY,
                parent_session_id TEXT NOT NULL,
                sister_session_id TEXT NOT NULL DEFAULT '',
                source_type     TEXT NOT NULL,
                source_id       TEXT NOT NULL,
                trigger_reason  TEXT NOT NULL,
                prompt          TEXT NOT NULL DEFAULT '',
                status          TEXT NOT NULL DEFAULT 'queued',
                priority        INTEGER NOT NULL DEFAULT 5,
                token_budget    INTEGER NOT NULL DEFAULT 10000,
                tokens_used     INTEGER NOT NULL DEFAULT 0,
                result_summary  TEXT NOT NULL DEFAULT '',
                result_body     TEXT NOT NULL DEFAULT '',
                actionable      INTEGER NOT NULL DEFAULT 0,
                confidence      REAL NOT NULL DEFAULT 0.0,
                created_at      TEXT NOT NULL,
                started_at      TEXT,
                completed_at    TEXT,
                error           TEXT
            );

            CREATE INDEX IF NOT EXISTS idx_tasks_parent_status
                ON proactive_tasks(parent_session_id, status);
            CREATE INDEX IF NOT EXISTS idx_tasks_priority
                ON proactive_tasks(status, priority DESC, created_at ASC);
            CREATE INDEX IF NOT EXISTS idx_tasks_source
                ON proactive_tasks(source_type, source_id);

            CREATE TABLE IF NOT EXISTS briefings (
                id              TEXT PRIMARY KEY,
                parent_session_id TEXT NOT NULL,
                source_type     TEXT NOT NULL,
                trigger_reason  TEXT NOT NULL,
                summary         TEXT NOT NULL,
                body            TEXT NOT NULL,
                actionable      INTEGER NOT NULL DEFAULT 0,
                confidence      REAL NOT NULL DEFAULT 0.0,
                status          TEXT NOT NULL DEFAULT 'unread',
                created_at      TEXT NOT NULL,
                read_at         TEXT,
                dismissed_at    TEXT
            );

            CREATE INDEX IF NOT EXISTS idx_briefings_parent_status
                ON briefings(parent_session_id, status);

            CREATE TABLE IF NOT EXISTS watches (
                id              TEXT PRIMARY KEY,
                parent_session_id TEXT NOT NULL,
                query           TEXT NOT NULL,
                check_type      TEXT NOT NULL DEFAULT 'search',
                check_target    TEXT NOT NULL DEFAULT '',
                interval_seconds INTEGER NOT NULL DEFAULT 3600,
                last_checked_at TEXT,
                next_check_at   TEXT NOT NULL,
                enabled         INTEGER NOT NULL DEFAULT 1,
                created_at      TEXT NOT NULL,
                metadata        TEXT NOT NULL DEFAULT '{}'
            );

            CREATE INDEX IF NOT EXISTS idx_watches_next
                ON watches(enabled, next_check_at);

            CREATE TABLE IF NOT EXISTS daily_budget (
                date            TEXT NOT NULL,
                session_id      TEXT NOT NULL,
                tokens_used     INTEGER NOT NULL DEFAULT 0,
                tasks_run       INTEGER NOT NULL DEFAULT 0,
                PRIMARY KEY (date, session_id)
            );
        """)
        await db.commit()

    async def clear_all(self) -> int:
        """Delete ALL proactive tasks, briefings, watches, and budget.  Returns total rows."""
        db = await self._get_db()
        total = 0
        for table in ("proactive_tasks", "briefings", "watches", "daily_budget"):
            cursor = await db.execute(f"DELETE FROM {table}")
            total += cursor.rowcount or 0
        await db.commit()
        return total

    # ── Task CRUD ────────────────────────────────────────────────────

    async def create_task(
        self,
        *,
        parent_session_id: str,
        source_type: str,
        source_id: str,
        trigger_reason: str,
        prompt: str = "",
        priority: int = 5,
        token_budget: int = 10000,
        sister_session_id: str = "",
    ) -> str:
        """Create a proactive task. Returns the task ID."""
        db = await self._get_db()
        task_id = uuid.uuid4().hex[:16]
        now = datetime.now(UTC).isoformat()
        await db.execute(
            """INSERT INTO proactive_tasks
               (id, parent_session_id, sister_session_id, source_type, source_id,
                trigger_reason, prompt, status, priority, token_budget, created_at)
               VALUES (?, ?, ?, ?, ?, ?, ?, 'queued', ?, ?, ?)""",
            (task_id, parent_session_id, sister_session_id, source_type,
             source_id, trigger_reason, prompt, priority, token_budget, now),
        )
        await db.commit()
        log.info("Proactive task created", task_id=task_id, source_type=source_type,
                 priority=priority, parent_session=parent_session_id)
        return task_id

    async def get_task(self, task_id: str) -> dict[str, Any] | None:
        """Get a single task by ID."""
        db = await self._get_db()
        async with db.execute(
            "SELECT * FROM proactive_tasks WHERE id = ?", (task_id,)
        ) as cur:
            row = await cur.fetchone()
            return dict(row) if row else None

    async def list_tasks(
        self,
        parent_session_id: str | None = None,
        *,
        status: str | None = None,
        limit: int = 50,
    ) -> list[dict[str, Any]]:
        """List tasks, optionally filtered by parent session and status."""
        db = await self._get_db()
        clauses: list[str] = []
        params: list[Any] = []
        if parent_session_id:
            clauses.append("parent_session_id = ?")
            params.append(parent_session_id)
        if status:
            clauses.append("status = ?")
            params.append(status)
        where = f"WHERE {' AND '.join(clauses)}" if clauses else ""
        query = f"SELECT * FROM proactive_tasks {where} ORDER BY priority DESC, created_at ASC LIMIT ?"
        params.append(limit)
        async with db.execute(query, params) as cur:
            return [dict(r) for r in await cur.fetchall()]

    async def update_task(self, task_id: str, **fields: Any) -> bool:
        """Update task fields."""
        if not fields:
            return False
        db = await self._get_db()
        sets = ", ".join(f"{k} = ?" for k in fields)
        vals = list(fields.values()) + [task_id]
        result = await db.execute(
            f"UPDATE proactive_tasks SET {sets} WHERE id = ?", vals,
        )
        await db.commit()
        return (result.rowcount or 0) > 0

    async def delete_task(self, task_id: str) -> bool:
        """Delete a task."""
        db = await self._get_db()
        result = await db.execute("DELETE FROM proactive_tasks WHERE id = ?", (task_id,))
        await db.commit()
        return (result.rowcount or 0) > 0

    async def get_next_task(self, parent_session_id: str) -> dict[str, Any] | None:
        """Get the highest-priority queued task for a parent session."""
        db = await self._get_db()
