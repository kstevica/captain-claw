    # ── Stats ────────────────────────────────────────────────────────

    async def stats(self, parent_session_id: str | None = None) -> dict[str, Any]:
        """Aggregate statistics."""
        db = await self._get_db()

        def _where(extra: str = "") -> tuple[str, list[Any]]:
            clauses: list[str] = []
            params: list[Any] = []
            if parent_session_id:
                clauses.append("parent_session_id = ?")
                params.append(parent_session_id)
            if extra:
                clauses.append(extra)
            w = ("WHERE " + " AND ".join(clauses)) if clauses else ""
            return w, params

        # Tasks
        w, p = _where()
        async with db.execute(f"SELECT COUNT(*) FROM proactive_tasks {w}", p) as cur:
            total_tasks = (await cur.fetchone())[0]

        w, p = _where("status = 'queued'")
        async with db.execute(f"SELECT COUNT(*) FROM proactive_tasks {w}", p) as cur:
            queued_tasks = (await cur.fetchone())[0]

        w, p = _where("status = 'done'")
        async with db.execute(f"SELECT COUNT(*) FROM proactive_tasks {w}", p) as cur:
            done_tasks = (await cur.fetchone())[0]

        # Briefings
        w, p = _where()
        async with db.execute(f"SELECT COUNT(*) FROM briefings {w}", p) as cur:
            total_briefings = (await cur.fetchone())[0]

        w, p = _where("status = 'unread'")
        async with db.execute(f"SELECT COUNT(*) FROM briefings {w}", p) as cur:
            unread_briefings = (await cur.fetchone())[0]

        # Today's budget
        today = date.today().isoformat()
        if parent_session_id:
            budget = await self.get_daily_budget(today, parent_session_id)
        else:
            async with db.execute(
                "SELECT COALESCE(SUM(tokens_used), 0), COALESCE(SUM(tasks_run), 0) FROM daily_budget WHERE date = ?",
                (today,),
            ) as cur:
                row = await cur.fetchone()
                budget = {"tokens_used": row[0], "tasks_run": row[1]}

        return {
            "total_tasks": total_tasks,
            "queued_tasks": queued_tasks,
            "done_tasks": done_tasks,
            "total_briefings": total_briefings,
            "unread_briefings": unread_briefings,
            "tokens_used_today": budget["tokens_used"],
            "tasks_run_today": budget["tasks_run"],
        }


