    # ── Daily Budget ─────────────────────────────────────────────────

    async def get_daily_budget(
        self,
        date_str: str,
        session_id: str,
    ) -> dict[str, int]:
        """Get daily budget usage."""
        db = await self._get_db()
        async with db.execute(
            "SELECT tokens_used, tasks_run FROM daily_budget WHERE date = ? AND session_id = ?",
            (date_str, session_id),
        ) as cur:
            row = await cur.fetchone()
            if row:
                return {"tokens_used": row[0], "tasks_run": row[1]}
            return {"tokens_used": 0, "tasks_run": 0}

    async def increment_daily_budget(
        self,
        date_str: str,
        session_id: str,
        tokens: int,
    ) -> None:
        """Increment daily budget counters."""
        db = await self._get_db()
        await db.execute(
            """INSERT INTO daily_budget (date, session_id, tokens_used, tasks_run)
               VALUES (?, ?, ?, 1)
               ON CONFLICT(date, session_id) DO UPDATE SET
                   tokens_used = tokens_used + excluded.tokens_used,
                   tasks_run = tasks_run + 1""",
            (date_str, session_id, tokens),
        )
        await db.commit()

