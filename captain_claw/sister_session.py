def get_session_sister_manager(session_id: str) -> SisterSessionManager:
    """Return a per-session manager (for public mode)."""
    if session_id not in _session_managers:
        cfg = get_config()
        base = Path(cfg.sister_session.db_path).expanduser().parent
        session_db = base / "sister_sessions" / f"sister_{session_id}.db"
        _session_managers[session_id] = SisterSessionManager(db_path=session_db)
    return _session_managers[session_id]


# ── Task Sourcing ────────────────────────────────────────────────────


def _score_intuition_priority(intuition: dict[str, Any]) -> int:
    """Calculate task priority from an intuition."""
    conf = float(intuition.get("confidence", 0.5))
    imp = int(intuition.get("importance", 5))
    tt = intuition.get("thread_type", "association")
    validated = bool(intuition.get("validated"))

    base = 4
    if tt == "hypothesis":
        base = 6 if conf >= 0.7 else 5
        if conf >= 0.9:
            base = 7
    elif tt == "pattern" and imp >= 8:
        base = 5
    elif tt == "connection" and conf >= 0.8:
        base = 4

    if validated:
        base += 1

    return min(base, 10)


def _should_trigger_task(intuition: dict[str, Any], cfg: Any = None) -> bool:
    """Decide whether an intuition should trigger a proactive task."""
    if cfg is None:
        cfg = get_config().sister_session

    tt = intuition.get("thread_type", "association")
    conf = float(intuition.get("confidence", 0.5))
    imp = int(intuition.get("importance", 5))

    # Hypotheses are investigatable — the primary trigger
    if tt == "hypothesis" and conf >= cfg.min_intuition_confidence:
        return True

    # High-importance patterns worth validating
    if tt == "pattern" and imp >= cfg.min_intuition_importance and conf >= cfg.min_intuition_confidence:
        return True

    # Strong connections tagged actionable
    if tt == "connection" and conf >= 0.8:
        tags = intuition.get("tags", "") or ""
        if "actionable" in tags.lower():
            return True

    # Associations — never (they're contextual, not investigatable)
    return False


async def maybe_create_proactive_task(
    parent_session_id: str,
    source_type: str,
    source_id: str,
    trigger_reason: str,
    priority: int = 5,
) -> str | None:
    """Create a proactive task if budget/dedup/limits allow. Returns task ID or None."""
    cfg = get_config()
    ss_cfg = cfg.sister_session

    if not ss_cfg.enabled:
        return None

    # Public mode guard
    if cfg.web.public_run and not ss_cfg.allow_public:
        return None

    # Resolve manager
    if cfg.web.public_run:
        mgr = get_session_sister_manager(parent_session_id)
    else:
        mgr = get_sister_session_manager()

    # 1. Dedup — don't create duplicate tasks for the same source
    existing = await mgr.find_task_by_source(source_type, source_id)
    if existing and existing["status"] in ("queued", "running", "done"):
        return None

    # 2. Rate limit — max tasks per hour per session
    recent_count = await mgr.count_recent_tasks(parent_session_id, since_seconds=3600)
    if recent_count >= ss_cfg.max_tasks_per_hour:
        log.info("Sister session rate limit hit", session=parent_session_id)
        return None

    # 3. Daily budget — check token budget
    today = date.today().isoformat()
    budget = await mgr.get_daily_budget(today, parent_session_id)
    if budget["tokens_used"] >= ss_cfg.max_tokens_per_day:
        log.info("Sister session daily budget exhausted", session=parent_session_id)
        return None

    # 4. Queue depth — don't pile up too many tasks
    queued_count = await mgr.count_tasks(parent_session_id, status="queued")
    if queued_count >= ss_cfg.max_queued_tasks:
        log.info("Sister session queue full", session=parent_session_id)
        return None

    # 5. Create the task
    task_id = await mgr.create_task(
        parent_session_id=parent_session_id,
        source_type=source_type,
        source_id=source_id,
        trigger_reason=trigger_reason,
        priority=priority,
        token_budget=ss_cfg.default_token_budget,
    )

    # Fire-and-forget: trigger execution of queued tasks
    if task_id:
        import asyncio
        asyncio.create_task(maybe_execute_tasks(parent_session_id))

    return task_id


