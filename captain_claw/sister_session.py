    async def create_watch(
        self,
        *,
        parent_session_id: str,
        query: str,
        check_type: str = "search",
        check_target: str = "",
        interval_seconds: int = 3600,
        metadata: dict[str, Any] | None = None,
    ) -> str:
        """Create a watch. Returns watch ID."""
        db = await self._get_db()
        watch_id = uuid.uuid4().hex[:16]
        now = datetime.now(UTC)
        next_check = (now + timedelta(seconds=interval_seconds)).isoformat()
        await db.execute(
            """INSERT INTO watches
               (id, parent_session_id, query, check_type, check_target,
                interval_seconds, next_check_at, enabled, created_at, metadata)
               VALUES (?, ?, ?, ?, ?, ?, ?, 1, ?, ?)""",
            (watch_id, parent_session_id, query, check_type, check_target,
             interval_seconds, next_check, now.isoformat(),
             json.dumps(metadata or {})),
        )
        await db.commit()
        log.info("Watch created", watch_id=watch_id, query=query[:60],
                 interval=interval_seconds)
