    async def update_watch_schedule(self, watch_id: str) -> None:
        """Advance a watch's next_check_at after it has been polled."""
        db = await self._get_db()
        now = datetime.now(UTC)
        # Read interval to compute next check
        async with db.execute(
            "SELECT interval_seconds FROM watches WHERE id = ?", (watch_id,),
        ) as cur:
            row = await cur.fetchone()
            if not row:
                return
            interval = row[0]
        next_check = (now + timedelta(seconds=interval)).isoformat()
        await db.execute(
            "UPDATE watches SET last_checked_at = ?, next_check_at = ? WHERE id = ?",
            (now.isoformat(), next_check, watch_id),
        )
        await db.commit()

