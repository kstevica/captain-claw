"""Sister Session — proactive autonomous work triggered by insights and dreams.

Each user session can have a paired "sister session" that receives tasks
generated by the nervous system (intuitions) and insights (deadlines).
Tasks are queued with priorities and, in Phase 2, executed autonomously
by a restricted agent.  Results are delivered as briefings.
"""

