def _register_sister_tools(agent: "Agent") -> None:
    """Register only safe, read-only tools for the sister agent."""
    from captain_claw.tools import (
        GlobTool,
        ReadTool,
        WebFetchTool,
        WebGetTool,
        WebSearchTool,
    )

    agent.tools.register(WebSearchTool())
    agent.tools.register(WebFetchTool())
    agent.tools.register(WebGetTool())
    agent.tools.register(ReadTool())
